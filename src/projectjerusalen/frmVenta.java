    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //Agregar datos a la tabla
        int fact = Integer.parseInt(txtNumVenta.getText());
        String prod = txtProducto.getText();
        double prec = Double.parseDouble(txtPrecioUni.getText());
        int cant = Integer.parseInt(txtCantidad.getText().toString());
        if(prod.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un productos");
        }
        else if(cant == 0 || cant < 0)
        {
            JOptionPane.showMessageDialog(null, "La cantidad debe ser mayor a 0");
        }
        else if(txtCantidad.getText().equals("")) 
        {
            JOptionPane.showMessageDialog(null, "Debe ingresar la cantidad a comprar");
        }
        else
        {
            vnt.llenarTabla(fact, prod, prec, cant, tableVenta, txtTotalVenta);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try{
            boolean valor;
            int idVenta = Integer.parseInt(txtNumVenta.getText().toString());
            int idTipo = cmbTipoVenta.getSelectedIndex();
            int idCliente = Integer.parseInt(txtBuscarClie.getText().toString());
            Date fecha = dateFormat.parse(txtFecha.getText());
            double total = Double.parseDouble(txtTotalVenta.getText().toString());
            valor = vnt.ingresarVenta(idVenta, idCliente, fecha, idTipo, total);
            if (valor == true)
            {
                boolean valor2;
                valor2 = vnt.ingresarDetalleVenta(tableVenta);
                if(valor2 == true)
                {
                    vnt.factura(idVenta);
                    JOptionPane.showMessageDialog(null, "Venta realizada correctamente");
                }
                else{
                    JOptionPane.showMessageDialog(null, "No se pudo ingresar el detalle de la venta");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "No se pudo ingresar la venta");
            }
        }catch(ParseException ex)
        {
            Logger.getLogger(frmCompra.class.getName()).log(Level.SEVERE, null, ex);
           
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

