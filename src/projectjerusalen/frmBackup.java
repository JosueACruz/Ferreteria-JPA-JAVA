    private void btnCarpetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarpetaActionPerformed
        String nombre = txtNombre.getText();
        if(txtNombre.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Debe ingresar el nombre del archivo de restauraci√≥n");
        }
        else{
            int rest = bc.generarbackup(nombre);
            if(rest == 1)
            {
                String n = nombre.trim();
                JOptionPane.showMessageDialog(null, "Puede buscar el backup en el directorio: C:/BackuOracle/"+n+".DMP");
            }
            else{
                JOptionPane.showMessageDialog(null, "Error al realizar la copia de seguridad.");
            }
        }
    }//GEN-LAST:event_btnCarpetaActionPerformed

